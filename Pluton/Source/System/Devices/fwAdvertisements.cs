using System;
using System.Collections.Generic;



namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------






    ///--------------------------------------------------------------------------------------
    /// обратный вызов рекламного блока
    public delegate void eventAds(bool completed);
    ///--------------------------------------------------------------------------------------








     ///=====================================================================================
    ///
    /// <summary>
    /// интерфейс
    /// Системы рекламы
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public interface IADS
    {
        ///--------------------------------------------------------------------------------------




         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, доступна сейчас реклама или нет
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        bool isAdAvailable();
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// показ рекламы, после ее показа запускаем обратный метод
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        bool show(eventAds signal_completed);
        ///--------------------------------------------------------------------------------------










    }
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------













     ///=====================================================================================
    ///
    /// <summary>
    /// Система групповой рекламы
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class AAdvertisements
        :
            IADS
    {
        ///--------------------------------------------------------------------------------------
        private readonly List<IADS> mAds = new List<IADS>(); //список рекламных компаний
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public AAdvertisements()
        {
           
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Добавить рекламу
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void add(IADS ads)
        {
            mAds.Add(ads);
        }
        ///--------------------------------------------------------------------------------------






 
         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, доступна сейчас реклама или нет
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isAdAvailable()
        {
            foreach (var obj in mAds)
            {
                if (obj.isAdAvailable())
                {
                    return true;
                }
            }
            return false;
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// показ рекламы, после ее показа запускаем обратный метод
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool show(eventAds signal_completed)
        {
            foreach (var obj in mAds)
            {
                if (obj.show(signal_completed))
                {
                    return true;
                }
            }
            return false;
        }
        ///--------------------------------------------------------------------------------------













    }
}
