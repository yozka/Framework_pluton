#region Using framework
using System;
using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Content;
using Microsoft.Xna.Framework.Graphics;
#endregion



namespace Pluton.SystemProgram
{
    ///--------------------------------------------------------------------------------------
    using Pluton.SystemProgram.Devices;
    ///--------------------------------------------------------------------------------------







     ///=====================================================================================
    ///
    /// <summary>
    /// Интерфейс фрейма, для быстрого поверхностного обновления, и отрисовки
    /// 
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public interface IScreenFrame
    {
        ///--------------------------------------------------------------------------------------






        
         ///=====================================================================================
        ///
        /// <summary>
        /// добавление в менеджер окон
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void connectManager(AScreenManager manager);
        ///--------------------------------------------------------------------------------------






        
         ///=====================================================================================
        ///
        /// <summary>
        /// уберание из менеджера окон
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void disconnectManager(AScreenManager manager);
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Обновление
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void update(TimeSpan gameTime);
        ///--------------------------------------------------------------------------------------










         ///=====================================================================================
        ///
        /// <summary>
        /// Отрисовка
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void draw(ASpriteBatch spriteBatch);
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Отрисовка
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void spriteUnload(int hContent);
        ///--------------------------------------------------------------------------------------












        ///--------------------------------------------------------------------------------------




    }
}
