#region Using framework
using System;
using System.Threading;
#endregion





namespace Pluton.Utils
{
    ///--------------------------------------------------------------------------------------
  




     ///=====================================================================================
    ///
    /// <summary>
    /// Блокировка потоков
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class AMutex
    {
        ///--------------------------------------------------------------------------------------
        private readonly Mutex mMutex = new Mutex();
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// Инциализация блокировки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public AMutex()
        {
        }
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// блокировка
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void mutexLock()
        {
            mMutex.WaitOne();
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// снятие блокировки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void mutexUnlock()
        {
            mMutex.ReleaseMutex();
        }
        ///--------------------------------------------------------------------------------------













    }//AMutex
}