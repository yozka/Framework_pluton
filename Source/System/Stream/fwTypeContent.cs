#region Using framework
using System;
using System.Collections.Generic;
using Microsoft.Xna.Framework;
#endregion



namespace Pluton.SystemProgram
{
    ///--------------------------------------------------------------------------------------








     ///=====================================================================================
    ///
    /// <summary>
    /// типы данных
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class ATypeContent
    {
        ///--------------------------------------------------------------------------------------
        private ushort mContent = 0;
        ///--------------------------------------------------------------------------------------




        private const ushort cPoint     = 0x0001;
        private const ushort cFloat     = 0x0002;
        private const ushort cString    = 0x0004;
        private const ushort cInteger   = 0x0008;
        private const ushort cUInteger  = 0x0010;
        private const ushort cBoolean   = 0x0020;
        private const ushort cVector2   = 0x0040;
        private const ushort cLong      = 0x0080;
        private const ushort cBinary    = 0x0100;
        private const ushort cChilds    = 0x0200;
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public ATypeContent()
        {
            
        }
        ///--------------------------------------------------------------------------------------




         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public ATypeContent(ushort type)
        {
            mContent = type;
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// данные контента
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public ushort typeContent
        {
            get
            {
                return mContent;
            }
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Point
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isPoint()
        {
            return (mContent & cPoint) == cPoint;
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Float
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isFloat()
        {
            return (mContent & cFloat) == cFloat;
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные String
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isString()
        {
            return (mContent & cString) == cString;
        }
        ///--------------------------------------------------------------------------------------




         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Integer
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isInteger()
        {
            return (mContent & cInteger) == cInteger;
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные UInteger
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isUInteger()
        {
            return (mContent & cUInteger) == cUInteger;
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Boolean
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isBoolean()
        {
            return (mContent & cBoolean) == cBoolean;
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Vector2
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isVector2()
        {
            return (mContent & cVector2) == cVector2;
        }
        ///--------------------------------------------------------------------------------------




         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Long
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isLong()
        {
            return (mContent & cLong) == cLong;
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Binary
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isBinary()
        {
            return (mContent & cBinary) == cBinary;
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// проверка, есть данные Childs
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool isChilds()
        {
            return (mContent & cChilds) == cChilds;
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setPoint(bool value)
        {
            setType(cPoint, value);
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setFloat(bool value)
        {
            setType(cFloat, value);
        }
        ///--------------------------------------------------------------------------------------




        
         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setString(bool value)
        {
            setType(cString, value);
        }
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setInteger(bool value)
        {
            setType(cInteger, value);
        }
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setUInteger(bool value)
        {
            setType(cUInteger, value);
        }
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setBoolean(bool value)
        {
            setType(cBoolean, value);
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setVector2(bool value)
        {
            setType(cVector2, value);
        }
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setLong(bool value)
        {
            setType(cLong, value);
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setBinary(bool value)
        {
            setType(cBinary, value);
        }
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void setChilds(bool value)
        {
            setType(cChilds, value);
        }
        ///--------------------------------------------------------------------------------------









         ///=====================================================================================
        ///
        /// <summary>
        /// установка данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        private void setType(int flag, bool value)
        {
            if (value)
            {
                mContent = (ushort)(mContent | flag);
            }
            else
            {
                mContent = (ushort)(mContent & ~flag);
            }
        }
        ///--------------------------------------------------------------------------------------


    }




}
