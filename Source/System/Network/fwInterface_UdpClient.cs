#region Using framework
using System;
#endregion





namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------
    




     ///=====================================================================================
    ///
    /// <summary>
    /// Сигнал который принимает датаграмма
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public delegate void eventUdpReceive(byte[] buffer, int size);
    ///--------------------------------------------------------------------------------------






     ///=====================================================================================
    ///
    /// <summary>
    /// Клиент датаграммы
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public interface IInteface_UdpClient
    {
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// есть или нет ошибки
        /// </summary>
        ///--------------------------------------------------------------------------------------
        bool isError();
        ///--------------------------------------------------------------------------------------




         ///=====================================================================================
        ///
        /// <summary>
        /// последняя ошибка
        /// </summary>
        ///--------------------------------------------------------------------------------------
        string lastError();
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// удалить ошибку
        /// </summary>
        ///--------------------------------------------------------------------------------------
        void clearError();
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// Проверка на предмет занятости сервера
        /// </summary>
        ///--------------------------------------------------------------------------------------
        bool isBussy();
        ///--------------------------------------------------------------------------------------



    


         ///=====================================================================================
        ///
        /// <summary>
        /// Подсоеденение к серверу
        /// </summary>
        ///--------------------------------------------------------------------------------------
        bool connect(string address, int port);
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Отсоеденение от сервера
        /// </summary>
        ///--------------------------------------------------------------------------------------
        void disconnect();
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// Отсылка данных
        /// </summary>
        ///--------------------------------------------------------------------------------------
        bool send(byte[] buffer, int length);
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// сигнал, чтения данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        event eventUdpReceive signal_receive;
        ///--------------------------------------------------------------------------------------






    }
    ///--------------------------------------------------------------------------------------
}
