#region Using framework
using System;
#endregion





namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------
    




     ///=====================================================================================
    ///
    /// <summary>
    /// Сигнал который принимает датаграмма
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public delegate void eventUdpReceive(byte[] buffer, IInteface_Address sender);
    ///--------------------------------------------------------------------------------------






     ///=====================================================================================
    ///
    /// <summary>
    /// Клиент датаграммы
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public interface IInteface_UdpClient
    {
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// Создание адреса
        /// </summary>
        ///--------------------------------------------------------------------------------------
        IInteface_Address createAddress(string url, int port);
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// Подсоеденение к серверу
        /// </summary>
        ///--------------------------------------------------------------------------------------
        bool connect(IInteface_Address address);
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Отсоеденение от сервера
        /// </summary>
        ///--------------------------------------------------------------------------------------
        void disconnect();
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// Отсылка данных
        /// </summary>
        ///--------------------------------------------------------------------------------------
        bool send(byte[] buffer, int length);
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// сигнал, чтения данных
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        event eventUdpReceive signal_receive;
        ///--------------------------------------------------------------------------------------






    }
    ///--------------------------------------------------------------------------------------
}
