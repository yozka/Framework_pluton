using System;
using System.Collections.Generic;



namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------









     ///=====================================================================================
    ///
    /// <summary>
    /// интерфейс
    /// Системы аналитики приложения
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public interface IAnalytics
    {
        ///--------------------------------------------------------------------------------------




         ///=====================================================================================
        ///
        /// <summary>
        /// Начало запуска программы
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void startSession();
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// выключение сесси
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void endSession();
        ///--------------------------------------------------------------------------------------



  


         ///=====================================================================================
        ///
        /// <summary>
        /// событие c параметрами
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void trackEvent(string eventName, IDictionary<string, string> properties);
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// ошибка
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void trackException(Exception ex);
        ///--------------------------------------------------------------------------------------



    }
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------













     ///=====================================================================================
    ///
    /// <summary>
    /// Система групповой аналитики приложения
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class AAnalytics_group
        :
            IAnalytics
    {
        ///--------------------------------------------------------------------------------------
        private readonly List<IAnalytics> mGroups = new List<IAnalytics>(); //список аналитик
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public AAnalytics_group()
        {
           
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Добавить аналитику
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void add(IAnalytics analytics)
        {
            mGroups.Add(analytics);
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Начало запуска программы
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void startSession()
        {
            foreach (var obj in mGroups)
            {
                obj.startSession();
            }
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// выключение сесси
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void endSession()
        {
            foreach (var obj in mGroups)
            {
                obj.endSession();
            }
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// событие c параметрами
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void trackEvent(string eventName, IDictionary<string, string> properties)
        {
            foreach (var obj in mGroups)
            {
                obj.trackEvent(eventName, properties);
            }
        }
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// ошибка
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void trackException(Exception ex)
        {
            foreach (var obj in mGroups)
            {
                obj.trackException(ex);
            }
        }
        ///--------------------------------------------------------------------------------------







    }
}
