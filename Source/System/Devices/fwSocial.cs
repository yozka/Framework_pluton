using System;
using System.Collections.Generic;



namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------









     ///=====================================================================================
    ///
    /// <summary>
    /// интерфейс
    /// Системы социальных взаимодействий
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public interface ISocial
    {
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// открытие ачивки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void openAchievement(string name);
        ///--------------------------------------------------------------------------------------













    }
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------













     ///=====================================================================================
    ///
    /// <summary>
    /// Система групповой социальной состовляющей
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class ASocial_group
        :
            ISocial
    {
        ///--------------------------------------------------------------------------------------
        private readonly List<ISocial> mGroups = new List<ISocial>(); //список аналитик
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public ASocial_group()
        {

        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Добавить социальную сеть
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void add(ISocial analytics)
        {
            mGroups.Add(analytics);
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// открытие ачивки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void openAchievement(string name)
        {
            foreach (var obj in mGroups)
            {
                obj.openAchievement(name);
            }
        }
        ///--------------------------------------------------------------------------------------
















        ///--------------------------------------------------------------------------------------
    }
}
