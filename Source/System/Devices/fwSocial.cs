using System;
using System.Collections.Generic;



namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------









     ///=====================================================================================
    ///
    /// <summary>
    /// интерфейс
    /// Системы социальных взаимодействий
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public interface ISocial
    {
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// открытие ачивки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void openAchievement(string name);
        ///--------------------------------------------------------------------------------------





         ///=====================================================================================
        ///
        /// <summary>
        /// переоткроем ачивки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void reopenAchievement();
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// Загрузка настроек
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void loadSettings(AStorage settings);
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// Сохранение настроек
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        void saveSettings(AStorage settings);
        ///--------------------------------------------------------------------------------------
       






    }
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------
    ///--------------------------------------------------------------------------------------













     ///=====================================================================================
    ///
    /// <summary>
    /// Система групповой социальной состовляющей
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class ASocial_group
        :
            ISocial
    {
        ///--------------------------------------------------------------------------------------
        private readonly List<ISocial> mGroups = new List<ISocial>(); //список аналитик
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public ASocial_group()
        {

        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Добавить социальную сеть
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void add(ISocial analytics)
        {
            mGroups.Add(analytics);
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// открытие ачивки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void openAchievement(string name)
        {
            foreach (var obj in mGroups)
            {
                obj.openAchievement(name);
            }
        }
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// Загрузка настроек
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void loadSettings(AStorage settings)
        {
            foreach (var obj in mGroups)
            {
                obj.loadSettings(settings);
            }
        }
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// Сохранение настроек
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void saveSettings(AStorage settings)
        {
            foreach (var obj in mGroups)
            {
                obj.saveSettings(settings);
            }
        }
        ///--------------------------------------------------------------------------------------







 
         ///=====================================================================================
        ///
        /// <summary>
        /// переоткроем ачивки
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void reopenAchievement()
        {
            foreach (var obj in mGroups)
            {
                obj.reopenAchievement();
            }
        }
        ///--------------------------------------------------------------------------------------




        ///--------------------------------------------------------------------------------------
    }
}
