#region Using framework
using System;
#endregion



namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------

    ///--------------------------------------------------------------------------------------







     ///=====================================================================================
    ///
    /// <summary>
    /// Глобальный доступ для вибрации
    /// 
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class gVibration
    {
    





         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public gVibration()
        {


        }
        ///--------------------------------------------------------------------------------------








        

         ///=====================================================================================
        ///
        /// <summary>
        /// начало вибрации
        /// 
        /// описание партитуры вибрации в милесекундах
        /// четные числа - вбирация
        /// нечетные числа - ожидание
        /// private readonly int[] m_vibSelectMenu = new int[] { 50, 100, 100 };
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public static void playOne(int[] tone)
        {
            var app = AApp.instance;
            if (app != null)
            {
                app.screenManager.devices.vibration.playOne(tone);
            }
        }
        ///--------------------------------------------------------------------------------------




















        ///--------------------------------------------------------------------------------------




    }
}
