#region Using framework
using System;
#endregion



namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------

    ///--------------------------------------------------------------------------------------







     ///=====================================================================================
    ///
    /// <summary>
    /// Глобальный доступ для звука
    /// 
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class gSound
    {
    






         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public gSound()
        {


        }
        ///--------------------------------------------------------------------------------------





        



         ///=====================================================================================
        ///
        /// <summary>
        /// проигрывание звуков
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public static void play(string name)
        {
            var app = AApp.instance;
            if (app != null)
            {
                app.screenManager.devices.sound.play(name);
            }
        }
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// проигрывание звуков только с одиночным
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public static void playOne(string name)
        {
            var app = AApp.instance;
            if (app != null)
            {
                app.screenManager.devices.sound.playOne(name);
            }
        }
        ///--------------------------------------------------------------------------------------

















        ///--------------------------------------------------------------------------------------




    }
}
