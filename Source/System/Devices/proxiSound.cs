#region Using framework
using System;
#endregion



namespace Pluton.SystemProgram.Devices
{
    ///--------------------------------------------------------------------------------------

    ///--------------------------------------------------------------------------------------







     ///=====================================================================================
    ///
    /// <summary>
    /// Глобальный доступ для звука
    /// 
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class gSound
    {
        ///--------------------------------------------------------------------------------------
        private static ASoundDevice mParent = null;
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// Constructor
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public gSound()
        {


        }
        ///--------------------------------------------------------------------------------------









        ///=====================================================================================
        ///
        /// <summary>
        /// привязка
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public static void setInstance(ASoundDevice parent)
        {
            if (mParent != null)
            {
                throw new Exception("Звук уже проброшен");
            }
            mParent = parent;
        }
        ///--------------------------------------------------------------------------------------










         ///=====================================================================================
        ///
        /// <summary>
        /// проигрывание звуков
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public static void play(string name)
        {
            if (mParent != null)
            {
                mParent.play(name);
            }
        }
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// проигрывание звуков только с одиночным
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public static void playOne(string name)
        {
            if (mParent != null)
            {
                mParent.playOne(name);
            }
        }
        ///--------------------------------------------------------------------------------------

















        ///--------------------------------------------------------------------------------------




    }
}
